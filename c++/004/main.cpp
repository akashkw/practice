#include <iostream>
using namespace std;

// 004
// size checker
// Check the size in bytes of multiple datatypes
// Prints out the data type and its size

int main() {
    cout << endl << "004 - size checker" << endl << endl;

    cout << "Size of char is " << sizeof(char) << " byte" << endl;
    cout << "Size of int is " << sizeof(int) << " bytes" << endl;
    cout << "Size of float is " << sizeof(float) << " bytes" << endl;
    cout << "Size of double is " << sizeof(double) << " bytes" << endl << endl;

    return 0;
}

